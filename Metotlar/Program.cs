            /*
             * Metot Nedir?
             * Her hangi bir görevi gerçekleştiren komutlar tanımlanan metot içersinde yazılır ve metoda bir isim verilir.
             * O göreve  programın her hangi bir yerinde ihtiyaç duyulduğunda metot ismi yazılarak ilgili komutlar çalıştırılarak görev yerine getirilmiş olur.
             * Kısacası bir programda aynı işlem tekrar tekrar yürütülüyorsa metotlardan faydalanılarak programın karmaşıklığının önüne geçilmiş olur.
             * 
             * !=> Main de programımızın ana metodudur. Bütün programlarda önce main metodu çalışır.
             * Diğer metotlar main metodunun içinden çağırılmadıkça çalışmaz.
             * 
             */


