             * METOT ERİŞİM BİÇİMİ
             *
             *Private: Yalnızca metodun yazıldığı sınıfta kullanılabilir.
             *Public : Metot programın tamamında kullanılabilir.
             *Static : Private ve Public ifadesinden sonra eklenirse metot doğrudan kullanılır.
             *ancak eklenmezse metodun bulunduğu sınıftan nesne türetilerek kullanılmak zorundadır.
