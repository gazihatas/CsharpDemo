             *GERİ DÖNÜŞ TİPİ
             * Eğer metot sonuç olarak geri değer göndermiyorsa buraya void ifadesi yazılmak zorundadır.
             * Ancak geri değer gönderiyorsa gönderilen değerin değişken türü buraya yazılmalıdır.
             * 
             *METOT İSMİ
             *Metot için iism belirlenir. 
             *Örneğin; alanHesapla(),ortamBul() gibi,
             * 
             * 
             *PARAMETRE
             *Metota dışarıdan değer gönderilmeyecekse parantez içi boş bırakılır, ancak dışarıdan değer gönderilecekse buraya gönderilecek değişkenler aralarında virgül konularak tanımlanır.
             *Örneğin; (int x, int y) gibi
             */
        }
